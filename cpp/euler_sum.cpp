#include <stdio.h>
int main()
{
	int n,i,s,d,r=0;double a;
	scanf("%d",&n);
	for(i=1;i<=n;i++)
	{
		a=(i*2.71828182845904523536028747135266249775724709369995);
		s=(int)a;r+=s;
		d=((int)((i+1)*2.71828182845904523536028747135266249775724709369995))-s;
		printf("%d\t%.40llf\t%d\t%d\t%d\n",i,a,s,d,r);
		if(i%7==0)
		printf("------------------------------\n");
	}
}
